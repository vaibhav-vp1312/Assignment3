public class Assi4_array_ques3 {
    public static void main(String[] args) {
        // reversed array
        int arr[]={1,2,3,4};
        System.out.println("Reversed array:");
        for (int i = arr.length - 1; i >= 0; i--){
            System.out.print(arr[i] + " ");
        }
      }
    }